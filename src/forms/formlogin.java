    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        // TODO add your handling code here:
        daoNV.reLoad();
        if (kiemthu()) {
            String username = txtUsername.getText().trim();
            String getpassword = "";
            if (txtPassword.isVisible()) {
                getpassword = txtPassword.getText();
            }
            if (password.isVisible()) {
                getpassword = new String(password.getPassword());
            }
            Nhanvien newnv = daoNV.find(username);
            if (newnv == null) {
                Dialog.ThongBao(this, "Tên đăng nhập không tồn tại");
            } else {
                if (newnv.getMatkhau().equals(getpassword)) {
                    Tienich.user = newnv;
                    Tienich.check = true;
                } else {
                    Dialog.ThongBao(this, "Mật khẩu không chính xác");
                }
            }
        }

    }//GEN-LAST:event_btnLoginMouseClicked

