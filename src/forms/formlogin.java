    public boolean kiemthu() {
        String username = txtUsername.getText().trim();
        if (username.equals("")) {
            Dialog.ThongBao(this, "Tên đăng nhập trống");
            return false;
        }
        String getpassword = "";
        if (txtPassword.isVisible()) {
            getpassword = txtPassword.getText();
        }
        if (password.isVisible()) {
            getpassword = new String(password.getPassword());
        }
        if (getpassword.equals("")) {
            Dialog.ThongBao(this, "Mật khẩu trống");
            return false;
        }

        return true;
    }

