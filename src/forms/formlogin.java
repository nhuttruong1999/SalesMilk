    @Override
    public void run() {
        while (true) {
            try {
                System.out.println("Running");
                if (Tienich.check) {
                    System.out.println("OK");
                    JDesktopPane forms = this.getDesktopPane();
                    formIdle form = new formIdle();
                    this.dispose();
                    forms.add(form);
                    form.show();
                    Dialog.ThongBao(this, "Đăng nhập thành công");
                    if (Tienich.user.getMatkhau().equals("0000")) {
                        Dialog.ThongBao(this, "Mật khẩu của bạn vừa được Reset, hãy đổi mật khẩu mới");
                    }
                    break;
                }
                Thread.sleep(500);
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
    }

